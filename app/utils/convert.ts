
// Feet to Meters Length Converter
export const feetToMeters = (feet) => {
  return feet / 3.2808
}

// Meters to Feet Length Converter
export const metersToFeet = (meters) => {
  return meters * 3.2808
}

// Centimeters to Meters Length Converter
export const cmToMeeters = (cm) => {
  return cm / 100
}
